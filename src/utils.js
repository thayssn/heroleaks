let debounceTimer;
export function debounce(fn, delay){
  return function (...args) {
    const callLaterFn = () => {
      fn(...args)
    }

    clearTimeout(debounceTimer)
    debounceTimer = setTimeout(callLaterFn, delay);
  }
}

